import mongoose from "mongoose";
import { IContact } from "./interface";

const contactSchema = new mongoose.Schema<IContact>(
  {
    name: {
      type: String,
      required: true,
    },
    email: {
      type: String,
      required: true,
    },
    phone: {
      type: String,
      default: "",
    },
    subject: {
      type: String,
      required: true,
    },
    message: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Contact = mongoose.model<IContact>("Contact", contactSchema);
export default Contact;
